package com.example.no16;

import java.util.Scanner;

public class no16 {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        System.out.println("請輸入阿拉伯數字 : ");
        int a = scanner.nextInt();
        if (a == 0)
            System.out.print("零");
        if (a == 1)
            System.out.print("壹");
        if (a == 2)
            System.out.print("貳");
        if (a == 3)
            System.out.print("參");
        if (a == 4)
            System.out.print("肆");
        if (a == 5)
            System.out.print("伍");
        if (a == 6)
            System.out.print("陸");
        if (a == 7)
            System.out.print("柒");
        if (a == 8)
            System.out.print("捌");
        if (a == 9)
            System.out.print("玖");
    }
}
